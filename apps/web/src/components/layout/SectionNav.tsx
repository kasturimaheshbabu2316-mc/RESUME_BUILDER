interface SectionNavProps {
  activeSection: SectionId
  onSectionChange: (section: SectionId) => void
}

export function SectionNav({ activeSection, onSectionChange }: SectionNavProps) {
  const [_expanded, setExpanded] = useState<SectionId>(activeSection)

  const handleClick = (id: SectionId) => {
    setExpanded(id)
    onSectionChange(id)
  }

  return (
    <nav aria-label="Resume sections" className="space-y-1">
      {sections.map((section) => {
        const isActive = activeSection === section.id
        return (
          <button
            key={section.id}
            onClick={() => handleClick(section.id)}
            className={`flex w-full items-center gap-3 rounded-md px-3 py-2.5 text-left text-sm font-medium transition-colors ${
              isActive
                ? 'bg-brand-50 text-brand-700 dark:bg-brand-900/30 dark:text-brand-300'
                : 'text-gray-600 hover:bg-gray-100 dark:text-gray-400 dark:hover:bg-gray-800'
            }`}
            aria-current={isActive ? 'page' : undefined}
          >
            <span className={isActive ? 'text-brand-600 dark:text-brand-400' : 'text-gray-400'}>
              {section.icon}
            </span>
            {section.label}
          </button>
        )
      })}
    </nav>
  )
}
